package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) 
	{
		//Print numbers 1 t0 5
		for(int i=1;i<=5;i++)
		{
			System.out.println(i);
			
		}
		System.out.println("-----------");
		//Print numbers 5 to 1
		for (int j =5; j >=1; j--) 
		{
			System.out.println(j);
		}

	}

}
